package homework_solution.lesson3.task1;

public class Calculator {

    int summ(int first, int second) {
        return first + second;
    }

    int substract(int first, int second) {
        return first - second;
    }

    int multiply(int first, int second) {
        return first * second;
    }

    int divide(int first, int second) {
        return first / second;
    }

}
