package homework_solution.lesson2.task8;

public class SwitchWithoutBreaks {

    public static void main(String[] args) {
        final int n = 3;
        switch (n) {
            default:
                System.out.print("A");
            case 4:
                System.out.print("A");
            case 3:
                System.out.print("A");
            case 2:
                System.out.print("A");
            case 1:
                System.out.print("A");
        }
    }

}
